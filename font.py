def to_binary_matrix(character):
  hex = font_hex[ord(character) - 32]
  binary_str = bin(hex)[2:].zfill(64)[::-1]
  return [map(int, list(chunk)) for chunk in  list(chunks(binary_str, 8))]

def chunks(s, n):
  """Produce `n`-character chunks from `s`."""
  for start in range(0, len(s), n):
    yield s[start:start+n]
